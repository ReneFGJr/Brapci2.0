			<h3>História da Biblioteca da Fabico - UFRGS</h3>
			<p>
				1959: A Biblioteca Setorial da Faculdade de Biblioteconomia e Comunicação (FABICO) iniciou seu funcionamento em 29 de setembro de 1959. Naquela época a faculdade era denominada Escola de Biblioteconomia e Documentação e o acervo da biblioteca era parte integrante da Biblioteca da Faculdade de Ciências Econômicas.
			</p>
			<p>
				1966: Em 23 de agosto de 1966 foi conferida a autonomia da Escola de Biblioteconomia. A partir desta data a Biblioteca passa a existir de fato, recebendo recursos e um espaço físico específico, mesmo que ainda instalada no prédio da Faculdade de Ciências Econômicas.
			</p>
			<p>
				1970: A partir da reforma universitária de 1970, ocorreu a junção dos Cursos de Biblioteconomia e Jornalismo quando a Escola passou a denominar-se Faculdade de Biblioteconomia e Comunicação, abrigando os dois cursos.
			</p>
			<p>
				1972: Em 1972 instalou-se no quarto andar do então novo prédio da Faculdade de Biblioteconomia e Comunicação, onde permanece até hoje. Neste ano passou a integrar o Sistema de Bibliotecas da UFRGS - SBU, tendo novas competências e responsabilidades perante a comunidade acadêmica. O seu acervo foi formado pelas obras de Biblioteconomia vindas da Faculdade de Ciências Econômicas e obras de Comunicação oriundas da Biblioteca da então Faculdade de Filosofia.
			</p>
			<p>
				1987: Em 1987 a Biblioteca passou por uma ampliação no seu espaço físico, de uma área de 135m² para 306m².
			</p>
			<p>
				2006: A partir de novembro de 2006, a Biblioteca passou a adotar uma linha de trabalho voltada para a Conservação Preventiva, de forma que todo e qualquer material impresso que venha a ser incorporado ao acervo, passe pelo Setor de Conservação e Restauração para ser preparado de forma a resistir ao uso contínuo e a permanecer por mais tempo sem sofrer qualquer tipo de intervenção na sua estrutura física. O acervo danificado pelo uso é continuamente avaliado e recolhido ao setor para ser recondicionado.
			</p>
			<p>
				2016: Criação de uma Sala de Consulta através da ligação da sala 408 à área da Biblioteca, promovendo ampliação da área de estudos e leitura para os usuários.
